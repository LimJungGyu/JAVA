package chap01;

public class Ex02 {

	public static void main(String[] args) {

		int x = 7;
		int y = 3;
		
		System.out.println(x+y);
		System.out.println(x-y);
		System.out.println(x*y);
		System.out.println(x/y); //정수 나누기 정수는 정수다 2
		System.out.println((double)x/y); //x를 실수로 만들어서 나누기
		System.out.println(x%y); //정수 나누기 정수의 나머지는 1
		
		
		
	}

}
